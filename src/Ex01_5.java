public class Ex01_5 {
	public static void main(String args[]) {
		int x = 10;					//변수 x에 10을 저장한다.
		int y = 5;					//변수 y에 5를 저장한다.
		System.out.println(x+y);	//저장된  x와  y를 이용하여 연산 후 출력한다.
		System.out.println(x-y);        
		System.out.println(x*y);        
		System.out.println(x/y);        
	}
}
